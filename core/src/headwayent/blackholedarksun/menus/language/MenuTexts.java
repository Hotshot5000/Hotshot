    public static void updateLanguage() {
        switch (Language.getSingleton().getCurrentLanguage()) {
            case ENGLISH:
                setEnglish();
                break;
            case ITALIAN:
                setItalian();
                break;
            default:
                throw new IllegalStateException("Unexpected value: " + Language.getSingleton().getCurrentLanguage());
        }
    }
